    public void Tabla() {
        DefaultTableModel columna = new DefaultTableModel();
        columna.addColumn("ID");
        columna.addColumn("NOMBRE");
        columna.addColumn("EDAD");
        columna.addColumn("CORREO");
        columna.addColumn("TELEFONO");
        for (Cliente c : cartera) {
            Object[] renglon = new Object[5];
            renglon[0] = c.getId();
            renglon[1] = c.getNombre();
            renglon[2] = c.getEdad();
            renglon[3] = c.getCorreo();
            renglon[4] = c.getTelefono();
            columna.addRow(renglon);
        }
        TABLA.setModel(columna);
    }

