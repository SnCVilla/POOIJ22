    private void jToggleButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jToggleButton1ActionPerformed
        Cliente cliente = new Cliente();
        cliente.setId(Integer.parseInt(ID.getText()));
        cliente.setNombre(NOMBRE.getText());
        cliente.setEdad(Integer.parseInt(EDAD.getText()));
        cliente.setTelefono(TELEFONO.getText());
        cliente.setCorreo(CORREO.getText());
        cartera.add(cliente);
        ID.setText("");
        NOMBRE.setText("");
        EDAD.setText("");
        TELEFONO.setText("");
        CORREO.setText("");
        Tabla();
    }//GEN-LAST:event_jToggleButton1ActionPerformed

    private void jToggleButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jToggleButton6ActionPerformed
        String preguntona = JOptionPane.showInputDialog("Escribeme a quien quieres eliminar Thanos :c ");
        for (Cliente c : cartera) {
            if (c.getNombre().equals(preguntona)) {
                cartera.remove(c);
                Tabla();
            }
        }
    }//GEN-LAST:event_jToggleButton6ActionPerformed

